function normalize(str: string): string {
  return str
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .toLowerCase();
}

export function searchCities(query: string, uf?: string): City[] {
  if (query.length < 2) return [];
  const normalizedQuery = normalize(query);
  return CITIES.filter(city => {
    if (uf && city.uf !== uf) return false;
    return normalize(city.name).includes(normalizedQuery);
  }).slice(0, 8);
}
